-- Количество исполнителей в каждом жанре.
SELECT Жанры.Название as Жанр, COUNT(Исполнители.Имя) as Количество_исполнителей
FROM Исполнители
JOIN Жанры ON Исполнители.Жанр_ID = Жанры.ID
GROUP BY Жанры.Название;

-- Количество треков, вошедших в альбомы 2019–2020 годов.
SELECT COUNT(Треки.Название) as Количество_треков
FROM Треки
JOIN Альбомы ON Треки.Альбом_ID = Альбомы.ID
WHERE Альбомы.Год_выпуска BETWEEN '2019-01-01' AND '2020-12-31';

-- Средняя продолжительность треков по каждому альбому.
SELECT Альбомы.Название as Альбом, AVG(Треки.Продолжительность) as Средняя_продолжительность
FROM Треки
JOIN Альбомы ON Треки.Альбом_ID = Альбомы.ID
GROUP BY Альбомы.Название;

-- Все исполнители, которые не выпустили альбомы в 2020 году.
SELECT Исполнители.Имя as Исполнитель
FROM Исполнители
LEFT JOIN Альбомы ON Исполнители.ID = Альбомы.Исполнитель_ID
WHERE Альбомы.Год_выпуска != '2020' OR Альбомы.ID IS NULL;

